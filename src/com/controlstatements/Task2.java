package com.controlstatements;

public class Task2 {

	public static void main(String[] args) {
		
		for (int j = 1; j <=3; j++) {

			for (int i = 1; i <=j; i++) {
				System.out.print(i);
		}
		System.out.println();
		}
		
		System.out.println();
		
		for (int j = 1; j <=3; j++) {

			for (int i = 1; i <=j; i++) {
				System.out.print("*");
		}
		System.out.println();
		}
	}
	
}
